const recent = document.querySelector('.recent');
const thisMonth = document.querySelector('section.this-month');
const lastMonth = document.querySelector('section.last-month');

const createLi = ({ title, description, img_url }) => {
    const li = document.createElement('li');
    const a = document.createElement('a');
    const div = document.createElement('div');
    const img = document.createElement('img');
    const span = document.createElement('span');
    const h3 = document.createElement('h3');
    const p = document.createElement('p');

    li.setAttribute('class', 'new');
    a.setAttribute('href', '#');
    div.setAttribute('class', 'picture');
    img.setAttribute('src', img_url);
    span.setAttribute('class', 'text-content');
    h3.appendChild(document.createTextNode(title));
    p.appendChild(document.createTextNode(description));
    span.appendChild(h3);
    span.appendChild(p);

    div.appendChild(img);
    div.appendChild(span);
    a.appendChild(div);
    li.appendChild(a);

    return li;
}

recentNews.map(item => {
    recent.querySelector('.news-container').appendChild(createLi(item));
});

thisMonthNews.map(item => {
    thisMonth.querySelector('.news-container').appendChild(createLi(item));
});

lastMonthNews.map(item => {
    lastMonth.querySelector('.news-container').appendChild(createLi(item));
});

document.querySelector('.news').addEventListener('mouseover', (e) => {
    const parent = e.target.parentNode;
    if( parent.getAttribute('class') === 'picture' ||
        parent.getAttribute('class') === 'text-content') {
        parent.querySelector('p').classList.toggle('on');
    }
});

document.querySelector('.news').addEventListener('mouseout', (e) => {
    const parent = e.target.parentNode;
    if( parent.getAttribute('class') === 'picture' ||
        parent.getAttribute('class') === 'text-content') {
        parent.querySelector('p').classList.toggle('on');
    }
});